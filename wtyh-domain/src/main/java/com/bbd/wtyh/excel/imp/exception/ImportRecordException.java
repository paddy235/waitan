package com.bbd.wtyh.excel.imp.exception;

/**
 * 导入记录异常类
 *
 * @author Created by LiYao on 2017-07-24 11:21.
 */
public class ImportRecordException extends Exception {

	public ImportRecordException() {
		super();
	}

	public ImportRecordException(String message) {
		super(message);
	}

	public ImportRecordException(String message, Throwable cause) {
		super(message, cause);
	}

	public ImportRecordException(Throwable cause) {
		super(cause);
	}

}
